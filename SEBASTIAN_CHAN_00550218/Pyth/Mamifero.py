class Mamifero:
    def __init__(self, nombre, peso):
        self.nombre = nombre
        self.peso = peso
    
    def setNombre(self, nombre):
        self.nombre = nombre
    
    def getNombre(self):
        return self.nombre
    
    def setPeso(self, peso):
        self.peso = peso
    
    def getPeso(self):
        return self.peso